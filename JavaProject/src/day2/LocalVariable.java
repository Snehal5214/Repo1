package day2;

public class LocalVariable {

	public static void main(String[] args) {
		System.out.println("Program Start");
		//syntax to declared & initialize a variable: datatype variablename = value;
		byte byteVariable = 25;
		short shortVariable = 55;
		int intVariable = 4556;
		float floatVaariable = 25.66f;
		double doubleVariable = 545.55d;
		char charVariable = 'Z';
		boolean booleanVariable = false;
		//print all the variable value in the console;
		System.out.println("byteVariable = "+byteVariable);
		System.out.println("shortVariable = "+shortVariable);
		System.out.println("intVariable = "+intVariable);
		System.out.println("floatVariable = "+floatVaariable);
		System.out.println("doubleVariable = "+doubleVariable);
		System.out.println("charVariable = "+charVariable);
		System.out.println("booleanVariable = "+booleanVariable);
		System.out.println("Program End");
		
	}

}
