package day2;

public class Example2 {

	public static void main(String[] args) {
		System.out.println("Program Start");
		//syntax to declared & intialize a variable: datatype variablename = value;
		//50 + 15 =65;
		int num1=50, num2=15;
		int result= num1 + num2;
		System.out.println("number 1 is "+num1);
		System.out.println("number 2 is "+num2);
		System.out.println("addition of two numbers is "+result);
		System.out.println("Program End");
		//syntax to declared & reintialize of result variable;
		result = num1 - num2;
		System.out.println("number 1 is "+num1);
		System.out.println("number 2 is "+num2);
		System.out.println("substraction of two number is "+result);
		System.out.println("Program End");
		//multiplication & re-initialization of result;
		result = num1*num2;
		System.out.println("number 1 is "+num1);
		System.out.println("number 2 is "+num2);
		System.out.println("multiplication of two number is "+result);
		System.out.println("Program End");
		//division & re-initialization of result;
		result= num1/num2;
		System.out.println("number 1 is "+num1);
		System.out.println("number 2 is "+num2);
		System.out.println("division of two number is "+result);
		System.out.println("Program End");
	}

}
